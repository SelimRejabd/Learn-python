# few useful method for string
name = "reja"

# lenth of string
print(len(name))

# finding a charecter
print(name.find("a"))

# Capitalize "reja" to "Reja"
print(name.capitalize())

# uppercase of a string
print(name.upper())

# lowecase of a string
print(name.lower())

# is string digit or not
print(name.isdigit())

# is string alphabatic or not
print(name.isalpha())

# count  charecter in string
print(name.count("e"))

# replace charecter
print(name.replace("j", "z"))

# string print multiple time
print(name*2)
